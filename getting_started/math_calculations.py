# Write Python expressions to represent the following situations:
# Find the square root of the sum of 8 and 43
# Find the integral part of the quotient when 100 is divided by 32
import math


sqrt_8 = math.sqrt(8)
sqrt_43 = math.sqrt(43) 

int_quotient = 100 // 32

print('Square root of 8 is', str(sqrt_8))
print('Square root of 43 is', str(sqrt_43))
print('Integer part of quotient of 100/32 is', str(int_quotient))